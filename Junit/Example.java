package Junit;

public class Example {

	public int add(int x, int y)
	{
		int sum =x+y;
		return sum;	
	}
	
	public int sub(int x, int y)
	{
		int diff =x-y;
		return diff;	
	}
	
	public int div(int x, int y)
	{
		int res =x/y;
		return res;	
	}
	public int mul(int x, int y)
	{
		int r =x*y;
		return r;	
	}
}
